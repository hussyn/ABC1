package overloading.concept;
public class ConstructorOverloading {

        private int number1;
        private int number2;

        public ConstructorOverloading(int number1) {
            this.number1 = number1;
        }

        public ConstructorOverloading(int number1, int number2) {
            this.number1 = number1;
            this.number2 = number2;
        }
        
}
