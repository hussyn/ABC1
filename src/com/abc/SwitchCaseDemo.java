package com.abc;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		int month = 0;
		switch(month) {
		case 1:
			System.out.println("case1");
		break;
		case 2:
			System.out.println("case2");
		break;
		default:
			System.out.println("Invalid month value passed");	
	}
	}

}
